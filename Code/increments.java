public class increments {
    public static void main(String[] args){
        int n = 10, b = 6;
        n-= 2;
        System.out.println("n = " + n);
        n = b + 3;
        System.out.println("n = " + n);
        --n;
        System.out.println("n = " + n);
        n++;
        System.out.println("n = " + n);
    }
}